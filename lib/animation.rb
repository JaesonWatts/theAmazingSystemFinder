

class Animation

  def self.scraperforchris
    system "clear"
    20.times do
      puts "scraping..........\\"
      sleep(0.03)
      system "clear"
      puts "scraping..........\\"
      sleep(0.03)
      system "clear"
      puts "scraping..........|"
      sleep(0.03)
      system "clear"
      puts "scraping........../"
      sleep(0.03)
      system "clear"
      puts "scraping..........-"
      sleep(0.03)
      system "clear"
    end
  end

  def self.loading_anim
    system "clear"
    21.times do
      puts "loading scraper..........\\"
      sleep(0.02)
      system "clear"
      puts "loading scraper..........\\"
      sleep(0.02)
      system "clear"
      puts "loading scraper..........|"
      sleep(0.02)
      system "clear"
      puts "loading scraper........../"
      sleep(0.02)
      system "clear"
      puts "loading scraper..........-"
      sleep(0.02)
      system "clear"
    end
  end

  def self.exit_anim
    system "clear"
    21.times do
      puts "exiting scraper..........\\"
      sleep(0.02)
      system "clear"
      puts "exiting scraper..........\\"
      sleep(0.02)
      system "clear"
      puts "exiting scraper..........|"
      sleep(0.02)
      system "clear"
      puts "exiting scraper........../"
      sleep(0.02)
      system "clear"
      puts "exiting scraper..........-"
      sleep(0.02)
      system "clear"
    end
  end
  


end
